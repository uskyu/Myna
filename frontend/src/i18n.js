const originalText = new WeakMap()
const originalAttributes = new WeakMap()
let observer = null
let translateQueued = false

function getInitialLanguage() {
  if (typeof window === 'undefined') return DEFAULT_LANGUAGE
  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY)
  if (languageCodes.has(saved)) return saved
  const browserLanguage = window.navigator.language || ''
  const matched = languages.find(language => browserLanguage.toLowerCase().startsWith(language.code.toLowerCase()))
  return matched?.code || DEFAULT_LANGUAGE
}

export const languageState = reactive({ current: getInitialLanguage() })
export const currentLanguage = computed(() => languages.find(language => language.code === languageState.current) || languages[0])

export function setLanguage(code) {
  languageState.current = languageCodes.has(code) ? code : DEFAULT_LANGUAGE
  if (typeof window !== 'undefined') window.localStorage.setItem(LANGUAGE_STORAGE_KEY, languageState.current)
  setPageAutoTranslateLanguage(languageState.current)
}

export function translateText(value, code = languageState.current) {
  if (!value || code === DEFAULT_LANGUAGE) return value
  const table = translations[code] || translations.en
  const compact = value.replace(/\s+/g, ' ').trim()
  if (!compact) return value
  if (table[compact]) return value.replace(compact, table[compact])
  if (translations.en[compact] && !table[compact]) return value.replace(compact, translations.en[compact])

  const iconPrefixed = compact.match(/^([⚙🧠🛠📊]+\s*)(.+)$/)
  if (iconPrefixed) {
    const translatedRest = table[iconPrefixed[2]] || translations.en[iconPrefixed[2]]
    if (translatedRest) return value.replace(compact, `${iconPrefixed[1]}${translatedRest}`)
  }

  const sortable = compact.match(/^(.+?)( [↑↓])$/)
  if (sortable) {
    const translatedBase = table[sortable[1]] || translations.en[sortable[1]]
    if (translatedBase) return value.replace(compact, `${translatedBase}${sortable[2]}`)
  }

  const timesPhrase = compact.match(/^(\d+) 次$/)
  if (timesPhrase) {
    const unit = table['次'] || translations.en['次'] || 'times'
    return value.replace(compact, `${timesPhrase[1]} ${unit}`)
  }

  const roundsPhrase = compact.match(/^(\d+) 轮$/)
  if (roundsPhrase) {
    const unit = table['轮'] || translations.en['轮'] || 'rounds'
    return value.replace(compact, `${roundsPhrase[1]} ${unit}`)
  }

  const daysPhrase = compact.match(/^(\d+) 天$/)
  if (daysPhrase) {
    const unit = table['天'] || translations.en['天'] || 'days'
    return value.replace(compact, `${daysPhrase[1]} ${unit}`)
  }

  const countPhrase = compact.match(/^管理模型供应商和可用配置。当前已有 (\d+) 个配置，桌面端可在这里直接新增、编辑或删除。$/)
  if (countPhrase) {
    const prefix = table['管理模型供应商和可用配置。当前已有'] || translations.en['管理模型供应商和可用配置。当前已有']
    const suffix = table['个配置，桌面端可在这里直接新增、编辑或删除。'] || translations.en['个配置，桌面端可在这里直接新增、编辑或删除。']
    if (prefix && suffix) return value.replace(compact, `${prefix} ${countPhrase[1]} ${suffix}`)
  }

  return value
}

function applyParams(text, params = {}) {
  return Object.entries(params).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), text)
}

export function tr(value, params) {
  return applyParams(translateText(value), params)
}

function restoreTextNode(node) {
  if (originalText.has(node)) node.nodeValue = originalText.get(node)
}

function translateTextNode(node) {
  if (!originalText.has(node)) originalText.set(node, node.nodeValue)
  node.nodeValue = translateText(originalText.get(node))
}

function restoreAttributes(element) {
  const attrs = originalAttributes.get(element)
  if (!attrs) return
  for (const [name, value] of Object.entries(attrs)) {
    if (element.getAttribute(name) !== value) element.setAttribute(name, value)
  }
}

function translateAttributes(element) {
  const names = ['title', 'placeholder', 'aria-label']
  const saved = originalAttributes.get(element) || {}
  let changed = false
  for (const name of names) {
    const currentValue = element.getAttribute(name)
    if (!currentValue) continue
    if (!(name in saved)) saved[name] = currentValue
    const translated = translateText(saved[name])
    if (translated !== currentValue) element.setAttribute(name, translated)
    changed = true
  }
  if (changed) originalAttributes.set(element, saved)
}

function shouldSkip(element) {
  return element.closest('script, style, code, pre, textarea, .msg-text, .markdown-body')
}

function translateNode(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    const parent = node.parentElement
    if (!parent || shouldSkip(parent)) return
    if (languageState.current === DEFAULT_LANGUAGE) restoreTextNode(node)
    else translateTextNode(node)
    return
  }

  if (node.nodeType !== Node.ELEMENT_NODE) return
  if (shouldSkip(node)) return
  if (languageState.current === DEFAULT_LANGUAGE) restoreAttributes(node)
  else translateAttributes(node)
  node.childNodes.forEach(translateNode)
}

export function translatePageNow() {
  if (typeof document === 'undefined') return
  if (observer) observer.disconnect()
  translateNode(document.body)
  if (observer) observer.observe(document.body, { childList: true, subtree: true, characterData: true, attributes: true, attributeFilter: ['title', 'placeholder', 'aria-label'] })
}

function queueTranslate() {
  if (translateQueued) return
  translateQueued = true
  requestAnimationFrame(() => {
    translateQueued = false
    translatePageNow()
  })
}

export function initI18n() {
  if (typeof document === 'undefined') return
  document.documentElement.lang = languageState.current
  document.documentElement.dir = languageState.current === 'ar' ? 'rtl' : 'ltr'
  observer = new MutationObserver(queueTranslate)
  observer.observe(document.body, { childList: true, subtree: true, characterData: true, attributes: true, attributeFilter: ['title', 'placeholder', 'aria-label'] })
  watchEffect(() => {
    document.documentElement.lang = languageState.current
    document.documentElement.dir = languageState.current === 'ar' ? 'rtl' : 'ltr'
    translatePageNow()
    setPageAutoTranslateLanguage(languageState.current)
  })
}


